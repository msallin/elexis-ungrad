package ch.codeblock.qrinvoice.layout;

import ch.codeblock.qrinvoice.util.NumberUtils;

public class Point<T extends Number> {
    private final T x;
    private final T y;

    public Point(final T x, final T y) {
        this.x = x;
        this.y = y;
    }

    public T getX() {
        return x;
    }

    public T getY() {
        return y;
    }

    /**
     * return true, if current point is (0,0)
     */
    public boolean isZeroPoint() {
        return NumberUtils.isZero(x) && NumberUtils.isZero(y);
    }

    public Point<T> move(final T xOffset, final T yOffset) {
        return new Point(NumberUtils.add(x, xOffset), NumberUtils.add(y , yOffset));
    }
}

